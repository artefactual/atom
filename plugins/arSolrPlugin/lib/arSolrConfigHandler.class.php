/**
 * arSolrConfigHandler.
 */
class arSolrConfigHandler extends sfYamlConfigHandler
{
    /**
     * Executes this configuration handler.
     *
     * @param array $configFiles An array of absolute filesystem path to a configuration file
     *
     * @return string Data to be written to a cache file
     */
    public function execute($configFiles)
    {
        // Parse the yaml
        $config = self::getConfiguration($configFiles);

        // Compile data
        $retval = "<?php\n"
            ."// auto-generated by %s\n"
            ."// date: %s\nreturn %s;\n";

        return sprintf($retval, __CLASS__, date('Y/m/d H:i:s'), var_export($config, true));
    }

    /**
     * @see sfConfigHandler
     */
    public static function getConfiguration(array $configFiles)
    {
        return self::replaceConstants(self::flattenConfigurationWithEnvironment(self::parseYamls($configFiles)));
    }
}
